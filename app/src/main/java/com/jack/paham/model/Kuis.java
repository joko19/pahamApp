package com.jack.paham.model;

public class Kuis {
    int skor, nyawa;

    public Kuis() {
    }

    public Kuis(int skor) {
        this.skor = skor;
    }

    public int getSkor() {
        return skor;
    }

    public void setSkor(int skor) {
        this.skor = skor;
    }

    public int getNyawa() {
        return nyawa;
    }

    public void setNyawa(int nyawa) {
        this.nyawa = nyawa;
    }
}
